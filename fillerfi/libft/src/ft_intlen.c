int		ft_intlen(int n)
{
	int		len;
	int		neg;

	len = 0;
	neg = 0;
	if (n < 0)
		neg = 1;
	if (!n)
		return (1);
	while (n)
	{
		n /= 10;
		len++;
	}
	return (len + neg);
}
