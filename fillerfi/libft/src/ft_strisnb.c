#include "libft.h"

int		ft_strisnb(const char *str)
{
	while (*str == '\n' || *str == '\v' || *str == ' '
			|| *str == '\t' || *str == '\r' || *str == '\f')
		str++;
	if (*str == '-' || *str == '+')
		str++;
	return (ft_isdigit(*str));
}
